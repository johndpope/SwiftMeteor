import UIKit

extension UIColor {
    func tabBarTintColor() -> UIColor {
        return UIColor.blue
    }
}
extension UIColor {
    static func candyGreen() -> UIColor {
        return UIColor(red: 67.0/255.0, green: 205.0/255.0, blue: 135.0/255.0, alpha: 1.0)
    }
}
